/*
You are given an array of integers nums (0-indexed) and an integer k.

The score of a subarray (i, j) is defined as min(nums[i], nums[i+1], ..., nums[j]) * (j - i + 1). A good subarray is a subarray where i <= k <= j.

Return the maximum possible score of a good subarray.

 

Example 1:

Input: nums = [1,4,3,7,4,5], k = 3
Output: 15
Explanation: The optimal subarray is (1, 5) with a score of min(4,3,7,4,5) * (5-1+1) = 3 * 5 = 15. 
Example 2:

Input: nums = [5,5,4,5,4,1,1,1], k = 0
Output: 20
Explanation: The optimal subarray is (0, 4) with a score of min(5,5,4,5,4) * (4-0+1) = 4 * 5 = 20.
 

Constraints:

1 <= nums.length <= 105
1 <= nums[i] <= 2 * 104
0 <= k < nums.length
*/

var maximumScore = function(nums, k) {
  let left = k;
  let right = k;
  let min = nums[k];
  let maxScore = nums[k];

  while (left >= 0 && right < nums.length) {
    min = Math.min(min, nums[left], nums[right]);
    maxScore = Math.max(maxScore, min * (right - left + 1));

    if(!left) {
      right += 1;
    } else if (right === nums.length - 1) {
      left -= 1;
    } else {
      const nextMinLeft = Math.min(min, nums[left - 1]);
      const nextMinRight = Math.min(min, nums[right + 1]);
      const nextDistance = right - left + 2;
      
      if (nextDistance * nextMinLeft > nextDistance * nextMinRight) {
        left -= 1;
      } else {
        right += 1;
      }
    }
  }

  return maxScore;
};

