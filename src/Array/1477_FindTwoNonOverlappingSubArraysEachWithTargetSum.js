/*
Given an array of integers arr and an integer target.

You have to find two non-overlapping sub-arrays of arr each with sum equal target. There can be multiple answers so you have to find an answer where the sum of the lengths of the two sub-arrays is minimum.

Return the minimum sum of the lengths of the two required sub-arrays, or return -1 if you cannot find such two sub-arrays.

 

Example 1:

Input: arr = [3,2,2,4,3], target = 3
Output: 2
Explanation: Only two sub-arrays have sum = 3 ([3] and [3]). The sum of their lengths is 2.
Example 2:

Input: arr = [7,3,4,7], target = 7
Output: 2
Explanation: Although we have three non-overlapping sub-arrays of sum = 7 ([7], [3,4] and [7]), but we will choose the first and third sub-arrays as the sum of their lengths is 2.
Example 3:

Input: arr = [4,3,2,6,2,3,4], target = 6
Output: -1
Explanation: We have only one sub-array of sum = 6.
Example 4:

Input: arr = [5,5,4,4,5], target = 3
Output: -1
Explanation: We cannot find a sub-array of sum = 3.
Example 5:

Input: arr = [3,1,1,1,5,1,2,1], target = 3
Output: 3
Explanation: Note that sub-arrays [1,2] and [2,1] cannot be an answer because they overlap.
 

Constraints:

1 <= arr.length <= 10^5
1 <= arr[i] <= 1000
1 <= target <= 10^8
*/

var minSumOfLengths = function (arr, target) {
  let start = 0;

  let currSum = 0;

  const minFromLeft = [];
  const minFromRight = [];

  let currMin;

  for (let curr = 0; curr < arr.length; curr += 1) {
    currSum += arr[curr];

    while (currSum > target) {
      currSum -= arr[start];
      start += 1;
    }

    if (currSum === target) {
      if (
        !currMin
        || curr - start + 1 < currMin[1]
      ) {
        currMin = [curr, curr - start + 1];
        minFromLeft.push(currMin);
      }

      minFromRight.push([start, curr - start + 1]);
    }
  }

  let minSum = Infinity;
  let left = minFromLeft.length - 1;
  for (let i = minFromRight.length - 1; i >= 0; i -= 1) {
    const [start, rLength] = minFromRight[i];
    while (
      left >= 0
      && minFromLeft[left][0] >= start
    ) left -= 1;

    if (left < 0) break;

    minSum = Math.min(minSum, minFromLeft[left][1] + rLength);
  }

  return minSum === Infinity ? -1 : minSum;
};

