/*
Reverse a linked list from position m to n. Do it in one-pass.

Note: 1 ≤ m ≤ n ≤ length of list.

Example:

Input: 1->2->3->4->5->NULL, m = 2, n = 4
Output: 1->4->3->2->5->NULL
*/

var reverseBetween = function (head, m, n) {
  if (m === n) return head;
  const start = new ListNode(null, head);
  let lead = start;
  let current;
  let follow = start;
  let count = 0;

  while (count < m) {
    follow = lead;
    lead = lead.next;
    count += 1;
  }

  current = lead;
  lead = lead.next;
  while (count < n) {
    const temp = lead.next;
    lead.next = current;
    current = lead;
    lead = temp;
    count += 1;
  }

  const temp = follow.next;
  follow.next = current;
  temp.next = lead;

  return start.next;
};

