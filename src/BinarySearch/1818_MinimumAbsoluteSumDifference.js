var minAbsoluteSumDiff = function(nums1, nums2) {
  const lookups = nums1.slice().sort((a, b) => a - b);
  
  let maxChange = 0;
  let totalDiff = 0;
  
  for (let i = 0; i < nums1.length; i += 1) {
    const diff = Math.abs(nums1[i] - nums2[i]);
    totalDiff += diff;
    
    if (diff > maxChange) {
      const closest = findClosest(lookups, nums2[i]);
      const newDiff = Math.abs(closest - nums2[i])
      maxChange = Math.max(maxChange, diff - newDiff)
    }
  }
  
  return totalDiff - maxChange;
};

const findClosest = (arr, target) => {
  let left = 0;
  let right = arr.length - 1;
  let mid = left;
  let closest = arr[mid];
  
  while (left < right) {
    mid = Math.floor((left + right) / 2);
    if(Math.abs(arr[mid] - target) < Math.abs(closest - target)) {
      closest = arr[mid]
    }
    
    if (arr[mid] - target < 0) {
      left = mid + 1;
    } else {
      right = mid;
    }
  }
  
  if (Math.abs(arr[right] - target) < Math.abs(closest - target)) {
    closest = arr[right]
  }

  return closest;
}


