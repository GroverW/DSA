var getMidRGB = function (colorName, colors) {
  const cToHex = (c) => {
    const hex = c.toString(16);
    return hex.length === 1 ? '0' + hex : hex;
  }

  const rgbToHex = (r, g, b) => {
    return '#' + cToHex(r) + cToHex(g) + cToHex(b);
  }

  const hexToRgb = (hex) => {
    const res = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
    return [parseInt(res[1], 16), parseInt(res[2], 16), parseInt(res[3], 16)]
  }

  const entries = Object.entries(colors);
  logOutList(colorName);
  for (let i = 0; i < entries.length - 1; i += 1) {
    const [keyA, hexA] = entries[i];
    const [keyB, hexB] = entries[i + 1];

    const [rA, bA, gA] = hexToRgb(hexA);
    const [rB, bB, gB] = hexToRgb(hexB);

    const midKey = Math.floor((+keyA + +keyB) / 2);
    const midHex = rgbToHex(
      Math.floor((rA + rB) / 2),
      Math.floor((gA + gB) / 2),
      Math.floor((bA + bB) / 2),
    );
    logOutList(`'${keyA}' => '${hexA}',`);
    logOutList(`'${midKey}' => '${midHex}',`.toUpperCase());
  }
  const [lastKey, lastHex] = entries[entries.length - 1];
  logOutList(`'${lastKey}' => '${lastHex}',`);
};

